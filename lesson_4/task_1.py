stars = int(input("Колко звездички искаш да видиш?: "))

# Първи вариант
# Стартираме от 0 и диапазонът завършва със stars. По този начин извършваме всички итерации.
for number in range(0, stars):
    print("*")

print("===========================================")

# Втори вариант
# Стартираме от 1 и диапазонът завършва със stars + 1, за да можем да включим и последното число.
for _ in range(1, stars + 1):
    print("*")